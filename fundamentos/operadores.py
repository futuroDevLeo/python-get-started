# Operadores Aritméticos

soma = 3 + 2
print(soma)

subtracao = 3 - 2
print(subtracao)

multiplicacao = 3 * 2
print(multiplicacao)

divisao = 3 / 2
print(divisao)

divisaoInteira = 3 // 2 #retorna a divisão como um número inteiro
print(divisaoInteira)

modulo = 3 % 2 #retorna o resto da divisão
print(modulo)

exponenciacao = 3 ** 2
print(exponenciacao)


