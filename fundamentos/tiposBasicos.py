# Variaveis e Tipos BÃ¡sicos

#inteiro
numero = 5
print(type(numero))

#ponto flutuante (real)
outroNumero = 1.2
print(type(outroNumero))

#string (texto)
nome = "leo"
print(type(nome))

#booleano
print(type(False))
print(type(True))

#lista
lista = [1, 2, 3]
print(type(lista))

#dicionario
dicionario = {'nome': 'leo', 'idade': 29}
print(type(dicionario))

#none (sem valor)
none = None
print(type(none))